function solution(n) {
  var answer = 0;
  let d = Array.from({ length: n }, () => 0);

  d[1] = 1;
  d[2] = 2;
  for (let i = 3; i <= n; i++) {
    d[i] = (d[i - 1] + d[i - 2]) % 1234567;
  }
  return d[n];
}

